<h1>Historia</h1>

<p><b>D’Erandi</b> es una empresa mexicana con 28 años de experiencia. Nace en Chiconcuac,
    Estado de México, como una empresa familiar dedicada, primera y únicamente,
    a la fabricación de blusones en tallas extra con un estilo simple.</p>

<p>Después de cinco años, <b>D’Erandi</b> cambió sus estilos sencillos a unos con mayor elaboración en  telas de temporada y moda.</p>

    <p>Gracias a la aceptación de los clientes a la nueva línea, nos dio la seguridad de crear
        <b>Richi Collection</b>, una línea dirigida a un segmento diferente. Esta apertura nos permitió ampliar el mercado de <b>D’Erandi</b>, diseñando blusas
        unitalla que conservan la misma calidad. La empresa fue creciendo para así crear dos nuevas marcas: <b>Innovaciones George</b>, especializada en blusas
        de talla grande; y <b>Dulce Bombón</b>, especializada en blusas unitalla con un toque más atrevido.</p>


<p>Hoy, Creaciones <b>D'erandi</b> está presente en toda la República, gracias a todos nuestros clientes y distribuidores.
    Contamos con puntos de venta en Chiconcuac (Estado de México),  Moroleón (Guanajuato), Texticuizeo (Michoacán)  y
    San Martín Texmelucan (Puebla).</p>

<p>El éxito de <b>D’Erandi</b> ha sido gracias a la fidelidad de nuestros clientes, quienes buscan calidad, exclusividad, moda y un excelente servicio.</p>
